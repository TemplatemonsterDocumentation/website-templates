<h3>.html and .css files editing</h3>

<p>All of editable HTML files are located at <b>"site"</b> folder (index.html, index-1.html, index-2.html, etc.). Every index-#.html file represents separate template page.</p>

<p>Sublime Text 3 is an advised application, for HTML files content editing, but you may also use any other suitable software.</p>

<p>In order to open .html file with Sublime Text 3 software, click with right mouse button on file icon and select <b>Open
        with > Sublime Text</b> 3 in dropdown menu. After file opening you can edit it.</p>

<p>CSS files are used to change template appearance, i.e., fonts (font-family, size, font-face, color, etc.), background, blocks width etc. CSS files can be edited with Sublime Text 3 software. </p>

<p class="alert alert-info">At our online support center you may find detailed tutorials on
    <a href="/help/general-tutorials/work-with-css/"> how to edit and manage .css files.</a></p>
