<h3>How to edit images</h3>

<p>Navigate to <b>'site/img'</b> folder. First way is to upload new images keeping the original filenames and extensions, simply replacing them.</p>

<p>Second way is to upload custom images specifying your own filenames and extensions.</p>

<p> Next you'll also need to change image filename in html.</p>

<ol class="index-list">
    <li>Open <b>.html</b> file within <b>“site”</b> folder by using html editor for this purpose.</li>
    <li>Use search function <b>(CTRL+F)</b> to locate an image in html file.</li>
    <li>Edit image filename and extension.</li>
</ol>

