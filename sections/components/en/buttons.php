<h6>HTML Website Templates buttons are defined by using 3 classes components:</h6>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>.btn</dt>
            <dd>main button class;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>.btn-xs, .btn-sm, .btn-md, .btn-lg, .btn-xl</dt>
            <dd> button layout classes, they are responsible for button size;</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>.btn-default, .btn-primary, .btn-secondary-*</dt>
            <dd>button styling classes, responsible for its text and background colors.</dd>
        </dl>
    </li>
</ul>

