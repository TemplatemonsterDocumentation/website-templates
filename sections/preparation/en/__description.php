<h4>Uploading the template to your server</h4>

<p>To get a live website, please upload all the contents of site/directory from your local PC to your server.</p>

<p class="alert alert-info">
    Attention: the root directory of a site depends on the internal structure of your hosting server.
    If you upload the <b>site/</b> folder to your server, your website root folder will be <b>http://your_domain_name/site</b>.
    To make <b>http://your_domain_name/</b> your root folder, please open the site/ folder and copy its contents to your server.
</p>

<p class="alert alert-danger">Attention: do not upload <b>sources/</b> folder or any of the template source files to your hosting server.</p>