<h2 class="item1">Preparation</h2>

<h5>Before installing Intense, you should be fully prepared. Please perform the following preparational steps:</h5>

<h3>Software</h3>

<p>To work with a website template, please download all the required applications. A list of all the necessary software can be found on the template preview page.
    Required software for different templates may vary, so we list the most basic ones:
</p>
<ol class="index-list">
    <li>
        First of all, you need the right extension for unzipping the password-protected sources_#########.zip archive. You can use <a
            href="http://www.winzip.com/downauto.cgi?o=1&file=http://download.winzip.com/winzip160.exe&email=&er=&os=win">WinZip
            9+</a> (Windows) or <a href="http://my.smithmicro.com/stuffit-deluxe-mac.html">Stuffit Expander
            10+</a> (Mac). This software is required.
    </li>
    <li>Moreover, you may need to have Adobe Photoshop (CS3 or a higher version). It’s used for editing .PSD source files and will help you change template graphical design and images.
    </li>
    <li>To edit the template source code, use a code editor, e.g. Sublime Text, Notepad++, Atom, etc.</li>
    <li>To upload files to your server, you need an FTP client, for example, cPanel File Manager or a desktop application, e.g. Total Commander, FileZilla, CuteFTP, etc.</li>
</ol>



<h3>Hosting</h3>

<p>You can run index.html file in the template folder, but you won’t see all its functionality. To see a fully working site, you must load your template to the hosting server.</p>

<p>If you have a live server, please check, whether it meets the Website Template requirements that are listed on the template preview page. For the majority of templates only PHP 5.2+ is required.</p>

<p>You can also install a Website Template on your PC with the help of a local server. To get a local hosting server, you can use software, such as WAMP, AppServ, MAMP, etc. These local servers are installed as usual software.</p>

