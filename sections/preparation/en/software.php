<h3>Software</h3>
<p>For proper work with the website template, we recommend downloading all the required software. The list of the necessary software is available on the template's preview page.<br>
   The requirements for various templates can be subject to change, so we will list the most basic ones:</p>

<ol class="index-list">
    <li>
        First of all, the correct software is required for extracting the password-protected archive <b>sources_#########.zip</b>. You can use <b>WinZip 9+</b> (Windows) and <b>Stuffit Expander 10+</b> (Mac). It is a mandatory software.
    </li>
    <li>
        You may also need <b>Adobe Photoshop</b> (CS3 or higher). it is used for editing the source files <b>*.psd</b> and is required for modifying the graphic design and images of the template.
    </li>
    <li>
        For editing the source code of template files, you need to use the code editor, e.g. <b>Sublime Text</b>, <b>Notepad++</b>, <b>Atom</b>, etc.
    </li>
    <li>
        To upload files to a hosting server, you will need an FTP manager, e.g. a file manager in the control panel of your hosting provider or desktop applications, such as <b>Total Commander</b>, <b>FileZilla</b>, <b>CuteFTP</b>, etc.
    </li>
    <li>
        If you are a developer or want to compile the source code, you will also need to install <a href='https://nodejs.org'>Node.js</a>.
    </li>
</ol>
