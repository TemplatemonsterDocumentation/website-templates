<h3>Хостинг</h3>
<p>
    Вы можете просто запустить файл <b>index.html</b> прямо из папки с сайтом, но Вы не увидите всего функционала шаблона. Для отображения полноценно работающего сайта необходимо загрузить шаблон на хостинг сервер.
    <br>
    При наличии активного сервера, нужно проверить его на соответствие требованиям <b>Website Template</b>, указанным на странице предпросмотра шаблона. Для большинства шаблонов обязательным требованием является только <b>PHP 5.2+</b>.
    <br>
    Также можно установить <b>Website Template</b> на Ваш компьютер при помощи локального сервера. Для создания локального хостинг сервера Вы можете использовать программное обеспечение <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b> и т.п. Любое из них устанавливается, как обычная программа.
</p>
