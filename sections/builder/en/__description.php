<h2 class="item3">HTML 5 Builder</h2>

<p>First and foremost, these feature-rich landing pages with drag-and-drop HTML editor are intended for end users who lack HTML or CSS knowledge. There is no need to bother with the backend – customization takes place at the frontend. Just drag and drop pre-defined blocks onto the blank canvas, edit them, add your own text, and voila! The page is ready to deliver the message of your marketing campaign. Besides end users, professional developers can also find these templates helpful, as they reduce the workflow duration to a minimum.</p>

<p>
  So, what is the HTML builder all about? This nifty customizer is built with HTML, CSS, JavaScript, and a single PHP file meant to export the markup you’ll create. The tool also makes use of a flat UI pro kit to help you design a clean and crispy layout. Once you enter its panel, you’ll see a burger menu with different pre-defined blocks on the left. You can move those blocks to the canvas by dragging and dropping, change their position the same way, and delete them individually or all in one go. In terms of editing, you can customize links, images, videos, icons, titles, text, and some other elements.</p>


<h3>Before Installing</h3>
<h4>Requirements</h4>

<p>To be able to use HTML Builder, you must have the following:</p>

<ul class="marked-list">
  <li>PHP</li>
  <li>Apache webserver (Windows servers will probably work, but are not supported)</li>
  <li>An FTP tool to upload the files</li>
</ul>

<h3>Installation </h3>

<p>Installing the HTML Builder script is simply a matter of uploading the script files to a server/hosting account. Once uploaded, point your browser to the said server/hosting and your script will work.</p>

<p>
  HTML Builder contains a variation of all sections of the template that you purchased. You can start building pages. It is enough to go to the builder folder on your hosting using the following link: http://yoursite.com/builder (please replace yoursite.com with actual website domain name)
</p>

