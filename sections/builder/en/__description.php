<h4>Using builder with your template</h4>

<ol class="index-list">
  <li>
    Launching builder for the first time you'll see white screen and the menu button in the top left corner. Menu contains all available for insertion and editing template sections in several variations.
  </li>

  <li>
    To start, you have to create your page by dragging the elements you need to white area in the center of the screen.
  </li>

  <li>
    <p>After creating the page out of the preset elements you can change text and images to the ones you prefer. To edit the text you should enable the option 'Building mode: Content' in the top left corner of the screen and click the element you want to edit.</p>

    <img src="img/builder-1.jpg" alt="">
  </li>


  <li>
    <p>To edit the styles, you have to activate the option 'Building mode: Details' and choose the element you want to edit.</p>

    <img src="img/builder-2.jpg" alt="">

    <p class="alert alert-warning">If the options area is empty, the active element has no styles available for editing</p>

  </li>

  <li>
    <p>After making edits you can export site by clicking 'Export' button in the top right corner of the screen.</p>

    <img src="img/builder-3.jpg" alt="">

    <p>Created archive can be uploaded to your hosting.</p>
  </li>
</ol>


