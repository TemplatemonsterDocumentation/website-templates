<h2 class="item3">Template customization</h2>

<h3>Smooth scroll configuration for Windows</h3>
<p>HTML Website Templates include built-in smooth scroll for Chrome browser in Windows OS. In order to activate it, just add
    <b>.smoothscroll</b> class to html tag.</p>
<code>&lt;html lang="en" class="smoothscroll"&gt;</code>

<p>As an option, you can also activate smooth scrolling for all platforms</p>
<code>&lt;html lang="en" class="smoothscroll-all"&gt;</code>

<h3>Template items animation on page scroll</h3>
<p>HTML Website Templates use <b>Wow.js</b> extension and <b>Animate.css</b> css library for the implementation of scroll animation. In order to enable scroll animation for entire page elements you should add
    <b>.wow-animation</b> class to html tag.</p>
<code>&lt;html lang="en" class="wow-animation"</code>
<p>You should also specify appropriate animation class for target item. Click <b><a href="//daneden.github.io/animate.css/">here</a></b> to check the list of available classes and live examples.</p>