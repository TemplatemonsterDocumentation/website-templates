<h3>Настройка плавной прокрутки страницы для Windows</h3>

<p>В HTML Website Templates имеется встроенная поддержка плавной прокрутки страницы для браузера Chrome в ОС Windows. Для того чтобы включить её, просто добавьте класс <b>.use--smoothscroll</b> для тега <b>html</b>:</p>
<pre><code>&lt;html lang="en" class="use--smoothscroll"&gt;</code></pre>
