<h3>Setting up a Shop</h3>
<p>Intense is an HTML template that targets developers. A Shop example in Intense is used just for simplifying the successive work on the
  product that developers carry out as a part of their orders. It does not contain functionality of implementing a live store.</p>

<p class="alert alert-warning">Attention: Intense does not have the functionality for creating a working online-shop.
  The Demo just has some examples of HTML marking for implementing a store based on Intense.</p>


