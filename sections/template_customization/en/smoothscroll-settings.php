<h3>Smooth scroll for Windows setup</h3>

<p>In HTML Website Templates, there is a built-in support of smooth scroll for Chrome browser in OS Windows. To enable it, just add the <b>.use--smoothscroll</b> class for the <b>html</b> tag.</p>
<pre><code>&lt;html lang="en" class="use--smoothscroll"&gt;</code></pre>
