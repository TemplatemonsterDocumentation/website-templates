<h3>Social Icons</h3>

<p>In order to define target links to customer's social networks accounts specify social network account page urls into relevant social links in href attribute.</p>

<p>For example,</p>

<code>&lt;a href=”YOUR_SOCIAL_LINK” class=”fa fa-facebook”&gt;&lt;/a&gt;</code>

<p>Where  <b>YOUR_SOCIAL_LINK</b> - Facebook page URL.</p>