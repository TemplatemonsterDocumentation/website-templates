<h3>Color schemes</h3>

<p>If template has several color schemes, it includes corresponding files colorScheme-1.css, colorScheme-2.css,
    colorScheme-#.css delivered to template owner within the theme package.</p>

<p>To activate desired color scheme initialize appropriate color schemes in &lt;head&gt; section of your html files,
    before the closing &lt;/head&gt; tag.</p>

<p>For example, to initialize colorScheme-1.css at index.html page, you should specify the following HTML markup in
    index.html file in &lt;head&gt; section before the closing &lt;/head&gt; tag:</p>

<code>
    &lt;head&gt;
    ….
    &lt;link rel=”stylesheet” href=”css/colorScheme-1.css”/&gt;
    &lt;/head&gt;
</code>

<p class="alert alert-warning">Color scheme switcher, visible at template's Live Demo, is used for preview purpose only
    and is not provided within template package.</p>