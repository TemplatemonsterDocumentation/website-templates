<h3>Youtube & Vimeo</h3>

<p>Use the following HTML code to embed Youtube, Vimeo video:</p>

<code>
    &lt;iframe class=”video” src="//player.vimeo.com/video/37582125?wmode=transparent" allowfullscreen&gt;&lt;/iframe&gt;
</code>

<p>where <b>src</b> attribute includes link to video at the relevant video-hosting.</p>