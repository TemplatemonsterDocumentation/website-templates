<h3>Youtube & Vimeo</h3>

<p>Для вставки видео из Youtube, Vimeo необходимо воспользовать следующим кодом:</p>

<code>
    &lt;iframe class=”video” src="//player.vimeo.com/video/37582125?wmode=transparent" allowfullscreen&gt;&lt;/iframe&gt;
</code>

<p>где, атрибут <b>src</b> содержит ссылку на видео в соответствующей сети.</p>