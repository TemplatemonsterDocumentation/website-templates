<h3>Социальные иконки</h3>

<p>Для того, чтобы указать целевые ссылки на пользовательские социальные аккаунты необходимо в сооветствующей
    социальному аккаунту ссылке в атрибуте href указать адрес страницы в социальной сети.</p>

<p>Например,</p>

<code>&lt;a href=”YOUR_SOCIAL_LINK” class=”fa fa-facebook”&gt;&lt;/a&gt;</code>

<p>Где, <b>YOUR_SOCIAL_LINK</b> - адрес страницы в Facebook.</p>