<h3>RD Navbar</h3>

<p>Данный скрипт предоставляет функционал для создания навигационной панели на мобильных устройствах.</p>

<p>Скрипт автоматически адаптируется к разметке заданной в HTML и считывает пункты меню заданные в вашей основной
    навигации для создания мобильного варианта.</p>

<p>В дополнительной настройке не нуждается.</p>

