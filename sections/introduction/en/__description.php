<h3>Validation</h3>

<p class="alert alert-info">Template's HTML and CSS files are semantically correct. However, some issues may appear
    while checking code via W3C service. It is related to CSS3 and HTML5 properties usage. We would like to provide you
    with the most user-friendly product of high quality, for this purpose it is required to implement latest rules, not
    yet adopted as standard. They are working totally cross-browser, just may be still not recognized as valid.</p>