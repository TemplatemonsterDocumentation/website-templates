<h4>Extending Intense with custom styles</h4>




<p>We recommend not to edit .css files that are embedded in the template. Open the <b>css/</b> folder of the template and create a custom <b>style-custom.css</b> file.
        Then, connect it to the target page in the <kbd>&lt;head&gt;</kbd> section with the help of the following command:</p>
<pre><code>&lt;link rel="stylesheet" href="css/style-custom.css"&gt;</code></pre>


