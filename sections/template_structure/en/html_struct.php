<h3>HTML Structure
    </h3>

<h6><b>.html</b> page structure of Intense templates is built the following way:</h6>

<pre><code>
&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;
    &lt;!-- Your Stylesheets, Core Scripts (jQuery etc) & Title --&gt;
    ...
&lt;/head&gt;
&lt;body&gt;
&lt;div class="page"&gt;
    &lt;!-- Page Head --&gt;
    &lt;header class="page-head"&gt;
        ...
    &lt;/header&gt;

    &lt;!-- Page Content--&gt;
    &lt;main class="page-content"&gt;
                ...
    &lt;/main&gt;

    &lt;!-- Page Footer--&gt;
    &lt;footer class="page-footer"&gt;
            ...
    &lt;/footer&gt;
&lt;/div&gt;
&lt;!-- Additional Functionality Scripts --&gt;
&lt;script src="js/script.js"&gt;&lt;/script&gt;
&lt;/body&gt;
&lt;/html&gt;
</code></pre>