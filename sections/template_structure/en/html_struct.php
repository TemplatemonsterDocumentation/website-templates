<h3>HTML Structure</h3>

<p>HTML Website Template <b>.html</b> pages structure is built on the following principle:</p>

<pre><code>&lt;!DOCTYPE html&gt;
&lt;html lang="en"&gt;
&lt;head&gt;

    &lt;!-- Your Stylesheets, Core Scripts (jQuery etc) & Title --&gt;
    ...

&lt;/head&gt;
&lt;body&gt;

&lt;!-- The Main Wrapper -->
&lt;div class="page"&gt;
    &lt;!--=================================================
                           Header
    ==================================================--&gt;
    &lt;header class="page-header"&gt;

        ...

    &lt;/header&gt;

    &lt;!--=================================================
                           Content
    ==================================================--&gt;
    &lt;main class="page-content"&gt;
        &lt;section&gt;
            &lt;div class="container"&gt;

                ...

            &lt;/div&gt;
        &lt;/section&gt;
    &lt;/main&gt;

    &lt;!--=================================================
                           Footer
    ==================================================--&gt;
    &lt;footer class="page-footer"&gt;
        &lt;div class="container"&gt;

            ...

        &lt;/div&gt;
    &lt;/footer&gt;
&lt;/div&gt;

&lt;!-- Additional Functionality Scripts --&gt;
&lt;script src="js/script.js"&gt;&lt;/script&gt;
&lt;/body&gt;
&lt;/html&gt;
</code></pre>