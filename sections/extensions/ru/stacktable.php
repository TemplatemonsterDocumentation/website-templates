<h3>Stacktable. Адаптивные таблицы</h3>
<p>Intense использует расширение <b>Stacktable</b> для реализации адаптивных таблиц.
    Для того чтобы сделать целевую таблицу адаптивной, достаточно просто добавить атрибут <b>data-responsive="true"</b> к ее разметке.</p>
<pre><code>
        &lt;table data-responsive="true"  &gt;
            ...
        &lt;/table&gt;
    </code></pre>