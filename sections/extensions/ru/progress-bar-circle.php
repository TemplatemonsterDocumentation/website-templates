<h3>Circle-progress. Радиальные полосы прогресса c градиентом</h3>
<h6>Intense использует расширение <b>jquery-circle-progress.js</b> для реализации радиальных полос прогресса с градиентом.</h6>
<p>Базовая HTML разметка для создания полосы прогресса выглядит следующим образом:</p>
<pre><code>
    &lt;!-- Circle Progress Bar--&gt;
    &lt;div data-value="0.8"
         data-gradient="#5aba67, #78c157"
         data-empty-fill="rgb(245,245,245)"
         data-size="150" class="progress-bar-circle"&gt;
      &lt;span&gt;&lt;/span&gt;
    &lt;/div&gt;
  </code></pre>
<p> гед:</p>
<ul class="marked-list">
  <li>
    <dl class="inline-term">
      <dt>data-value</dt>
      <dd>значение полосы прогресса (обязательный атрибут);</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>data-gradient</dt>
      <dd>цвет заполненного пути полосы прогресса (обязательный атрибут);</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>data-size</dt>
      <dd>размер полосы прогресса в пикселях;</dd>
    </dl>
  </li>
  <li>
    <dl class="inline-term">
      <dt>data-empty-fill</dt>
      <dd>цвет незаполненного пути полосы прогресса.</dd>
    </dl>
  </li>
</ul>







